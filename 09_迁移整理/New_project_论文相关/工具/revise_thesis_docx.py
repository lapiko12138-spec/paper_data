def iter_all_paragraphs(doc: Document):
    for para in doc.paragraphs:
        yield para
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for para in cell.paragraphs:
                    yield para


def find_paragraph(doc: Document, prefix: str) -> Paragraph:
    matches = [para for para in doc.paragraphs if para.text.strip().startswith(prefix)]
    if len(matches) != 1:
        raise ValueError(f"Expected 1 paragraph for prefix {prefix!r}, found {len(matches)}")
    return matches[0]


def replace_paragraph(doc: Document, prefix: str, new_text: str) -> None:
    find_paragraph(doc, prefix).text = new_text


def insert_paragraph_after(paragraph: Paragraph, text: str, style_name: str | None = None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style_name:
        new_para.style = style_name
    new_para.text = text
    return new_para


def replace_everywhere(doc: Document, old: str, new: str) -> None:
    for para in iter_all_paragraphs(doc):
        if old in para.text:
            para.text = para.text.replace(old, new)


